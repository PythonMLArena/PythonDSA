# queue open at both ends 
# FIFO Methodologies
# front/head (remove elements)
# back/tail (adding elements)

# enqueue (Adding elements)
# dequeue (Removing elements)

queue=[]

queue.append(10)
queue.insert(0,10) # insert from other end

queue.pop(0) # zero index , remove elements from front 
queue.pop() # remove elements from other end


